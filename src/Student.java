    private void update_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_buttonActionPerformed
        // Update button code:

        try {
            stmt = conn.createStatement();
            String stid = id.getText();
            String stdName = std_name.getText();
            String stdaddress = std_address.getText();
            String stdMail = std_email.getText();
            String stPhone = std_phone.getText();
            String ssubject = subject_scheme.getText();
            String parentNme = std_parentname.getText();
            if (stid.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Plese enter Stid");

            } else {
                if (stPhone.isEmpty()) {
                    stPhone = "000000000";
                }

                String sql = "UPDATE STUDENTD SET id ='" + Integer.parseInt(stid) + "', std_name ='" + stdName + "', std_address ='" + stdaddress + "',std_email ='" + stdMail + "', std_phone='" + Integer.parseInt(stPhone) + "', std_parentname ='" + parentNme + "', subject_scheme ='" + ssubject + "' WHERE iD = '" + Integer.parseInt(stid) + "'";

                int excett = stmt.executeUpdate(sql);
                if (excett == 1) {
                    JOptionPane.showMessageDialog(null, "Data update Successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Data update Not Successful");
                }

            }

            id.setText("");
            std_name.setText("");
            std_address.setText("");
            std_email.setText("");
            std_phone.setText("");
            subject_scheme.setText("");
            std_parentname.setText("");

        } catch (java.sql.SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }
        setVisible(false);
        Student object = new Student();
        object.setVisible(true);

    }//GEN-LAST:event_update_buttonActionPerformed

