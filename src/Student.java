        int sid = (Integer) jTable2.getValueAt(jTable2.getSelectedRow(), 0);

        // System.out.println(sid);
        String name = (String) jTable2.getValueAt(jTable2.getSelectedRow(), 1);
        String address = (String) jTable2.getValueAt(jTable2.getSelectedRow(), 2);
        int phone = (Integer) jTable2.getValueAt(jTable2.getSelectedRow(), 3);
        String email = (String) jTable2.getValueAt(jTable2.getSelectedRow(), 4);
        String subject = (String) jTable2.getValueAt(jTable2.getSelectedRow(), 5);
        String pname = (String) jTable2.getValueAt(jTable2.getSelectedRow(), 6);

        id.setText(sid + "");
        std_name.setText(name);
        std_address.setText(address);
        std_email.setText(email);
        std_phone.setText(phone + "");
        subject_scheme.setText(pname);
        std_parentname.setText(subject);


